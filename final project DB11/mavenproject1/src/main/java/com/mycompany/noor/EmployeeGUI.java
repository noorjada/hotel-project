private void removeEmployeeFromDatabase(int employeeId, String departmentName) {
    try {
       
                   DriverManager.registerDriver(new org.postgresql.Driver ());
                Connection connection = DriverManager.getConnection("jdbc:postgresql://localhost:5432/DB", "postgres", "123");

           
            String deleteEmployeeSql = "DELETE FROM employee WHERE EmployeeID = ?";
            String updateDepartmentSql = "UPDATE department SET dep_employees = dep_employees - 1 WHERE dep_name = ?";
            try (PreparedStatement deleteEmployeeStatement = connection.prepareStatement(deleteEmployeeSql);
                    PreparedStatement updateDepartmentStatement = connection.prepareStatement(updateDepartmentSql)) {
                
                
                deleteEmployeeStatement.setInt(1, employeeId);
                
                
                int rowsDeleted = deleteEmployeeStatement.executeUpdate();
                
                if (rowsDeleted > 0) {
                    
                    javax.swing.JOptionPane.showMessageDialog(this, "Employee removed successfully", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                    
                    
                    updateDepartmentStatement.setString(1, departmentName);
                    int rowsUpdated = updateDepartmentStatement.executeUpdate();
                    
                    if (rowsUpdated <= 0) {
                        javax.swing.JOptionPane.showMessageDialog(this, "Failed to update department", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Failed to remove employee", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                }
            }
        
    } catch (SQLException ex) {
      
        ex.printStackTrace();
    }
}

    private void displayEmployeeInformation() {
    try {
         DriverManager.registerDriver(new org.postgresql.Driver ());
        Connection connection = DriverManager.getConnection("jdbc:postgresql://localhost:5432/DB", "postgres", "123");
           
            String sql = "SELECT * FROM Employee";
            try (java.sql.PreparedStatement statement = connection.prepareStatement(sql)) {
                
                try (java.sql.ResultSet resultSet = statement.executeQuery()) {
                    
                    DefaultTableModel tableModel = new DefaultTableModel();
                    
                    
                    
                    tableModel.addColumn("name");
                    tableModel.addColumn("employeeid");
                    tableModel.addColumn("age");
                    tableModel.addColumn("gender");
                    tableModel.addColumn("Job Title");
                    tableModel.addColumn("Salary");
                    tableModel.addColumn("dep_name");
                    tableModel.addColumn("Email");
                    tableModel.addColumn("Phone");
                    tableModel.addColumn("dep_no");
                    
                    
                    
                    
                    while (resultSet.next()) {
                        Object[] rowData = {
                            
                            resultSet.getString("name"),
                            resultSet.getInt("employeeid"),
                            resultSet.getInt("age"),
                            resultSet.getString("gender"),
                            resultSet.getString("Job_title"),
                            resultSet.getDouble("Salary"),
                            resultSet.getString("dep_name"),
                            resultSet.getString("Email"),
                            resultSet.getString("Phone"),
                            resultSet.getInt("dep_no")
                                
                                
                        };
                        tableModel.addRow(rowData);
                    }
                    
                    
                    JTable employeeTable = new JTable(tableModel);
                    
                    
                    JOptionPane.showMessageDialog(this, new JScrollPane(employeeTable), "Employee Information", JOptionPane.PLAIN_MESSAGE);
                }
            }
        
    } catch (java.sql.SQLException ex) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
}
   private void updateEmployeeInfo() {
    try {
              DriverManager.registerDriver(new org.postgresql.Driver ());
       Connection connection = DriverManager.getConnection("jdbc:postgresql://localhost:5432/DB", "postgres", "123");
            
            String sql = "UPDATE Employee SET phone = ?, salary = ?, job_title = ?, email = ? WHERE EmployeeID = ?";
            try (PreparedStatement statement = connection.prepareStatement(sql)) {
                
                String employeeIdInput = JOptionPane.showInputDialog(this, "Enter Employee ID:", "Update Employee Information", JOptionPane.QUESTION_MESSAGE);
                String newPhoneInput = JOptionPane.showInputDialog(this, "Enter New Phone:", "Update Employee Information", JOptionPane.QUESTION_MESSAGE);
                String newSalaryInput = JOptionPane.showInputDialog(this, "Enter New Salary:", "Update Employee Information", JOptionPane.QUESTION_MESSAGE);
                String newJobTitleInput = JOptionPane.showInputDialog(this, "Enter New Job Title:", "Update Employee Information", JOptionPane.QUESTION_MESSAGE);
                String newEmailInput = JOptionPane.showInputDialog(this, "Enter New Email:", "Update Employee Information", JOptionPane.QUESTION_MESSAGE);
                
                if (employeeIdInput != null && newPhoneInput != null &&
                        newSalaryInput != null && newJobTitleInput != null && newEmailInput != null &&
                        !employeeIdInput.isEmpty() && !newPhoneInput.isEmpty() &&
                        !newSalaryInput.isEmpty() && !newJobTitleInput.isEmpty() && !newEmailInput.isEmpty()) {
                    
                    int employeeId = Integer.parseInt(employeeIdInput);
                    
                    
                    statement.setString(1, newPhoneInput);
                    statement.setDouble(2, Double.parseDouble(newSalaryInput));
                    statement.setString(3, newJobTitleInput);
                    statement.setString(4, newEmailInput);
                    
                    statement.setInt(5, employeeId);
                    
                    
                    int rowsAffected = statement.executeUpdate();
                    
                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(this, "Employee information updated successfully!");
                    } else {
                        JOptionPane.showMessageDialog(this, "Employee not found or information update failed.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}



