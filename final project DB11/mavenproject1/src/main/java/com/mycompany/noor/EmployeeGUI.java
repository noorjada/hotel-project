private void deleteEmployee() {
    try {
        
        DriverManager.registerDriver(new org.postgresql.Driver ());
            Connection connection = DriverManager.getConnection("jdbc:postgresql://localhost:5432/DB", "postgres", "123");
            String employeeIdInput = JOptionPane.showInputDialog(this, "Enter Employee ID to delete:", "Delete Employee", JOptionPane.QUESTION_MESSAGE);
            
            if (employeeIdInput != null && !employeeIdInput.isEmpty()) {
                int employeeId = Integer.parseInt(employeeIdInput);
                
                
                int departmentNumber = getDepartmentNumberForEmployee(connection, employeeId);
                
                
                deleteEmployee(connection, employeeId);
                
                
                decrementDepartmentEmployeesCount(connection, departmentNumber);
                
                
                JOptionPane.showMessageDialog(this, "Employee deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            }
        }
     catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}


private void deleteEmployee(Connection connection, int employeeId) throws SQLException {
    String deleteSql = "DELETE FROM employee WHERE employeeid = ?";
    try (PreparedStatement deleteStatement = connection.prepareStatement(deleteSql)) {
        deleteStatement.setInt(1, employeeId);
        deleteStatement.executeUpdate();
    }
}


private int getDepartmentNumberForEmployee(Connection connection, int employeeId) throws SQLException {
    String sql = "SELECT dep_no FROM Employee WHERE EmployeeID = ?";
    try (PreparedStatement statement = connection.prepareStatement(sql)) {
        statement.setInt(1, employeeId);
        try (ResultSet resultSet = statement.executeQuery()) {
            if (resultSet.next()) {
                return resultSet.getInt("dep_no");
            }
            return -1;
        }
    }
}


private void decrementDepartmentEmployeesCount(Connection connection, int departmentNumber) throws SQLException {
    String updateSql = "UPDATE Department SET dep_employees = dep_employees - 1 WHERE Dep_num = ?";
    try (PreparedStatement updateStatement = connection.prepareStatement(updateSql)) {
        updateStatement.setInt(1, departmentNumber);
        updateStatement.executeUpdate();
    }
}

