private void updateRoomStatus() {
    try {
              DriverManager.registerDriver(new org.postgresql.Driver ());
        Connection connection = DriverManager.getConnection("jdbc:postgresql://localhost:5432/DB", "postgres", "123");

        
        String sql = "UPDATE Room SET avalbility = ?, cleaning_status = ?, price = ? WHERE RoomNum = ?";

        try (PreparedStatement statement = connection.prepareStatement(sql)) {
            
            String roomNumInput = JOptionPane.showInputDialog(this, "Enter Room Number:", "Update Room Status", JOptionPane.QUESTION_MESSAGE);
            String newStatusInput = JOptionPane.showInputDialog(this, "Enter New Status (Vacant or Occupied):", "Update Room Status", JOptionPane.QUESTION_MESSAGE);
            String newCleaningStatusInput = JOptionPane.showInputDialog(this, "Enter New Cleaning Status:", "Update Room Status", JOptionPane.QUESTION_MESSAGE);
            String newPriceInput = JOptionPane.showInputDialog(this, "Enter New Price:", "Update Room Status", JOptionPane.QUESTION_MESSAGE);

            if (roomNumInput != null && newStatusInput != null && newCleaningStatusInput != null && newPriceInput != null &&
                !roomNumInput.isEmpty() && !newStatusInput.isEmpty() && !newCleaningStatusInput.isEmpty() && !newPriceInput.isEmpty()) {
                int roomNum = Integer.parseInt(roomNumInput);
                String newStatus = newStatusInput.trim().toLowerCase();
                String newCleaningStatus = newCleaningStatusInput.trim();
                double newPrice = Double.parseDouble(newPriceInput);

                
                statement.setString(1, newStatus);
                statement.setString(2, newCleaningStatus);
                statement.setDouble(3, newPrice);
                statement.setInt(4, roomNum);

             
                int rowsAffected = statement.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, "Room information updated successfully!");
                } else {
                    JOptionPane.showMessageDialog(this, "Room not found or information update failed.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        }

        
        connection.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
}

