private void checkOutGuest() {
    try {
            DriverManager.registerDriver(new org.postgresql.Driver ());
        Connection connection = DriverManager.getConnection("jdbc:postgresql://localhost:5432/DB", "postgres", "123");

      
        String sql = "SELECT * FROM Guest";

        try (PreparedStatement statement = connection.prepareStatement(sql)) {
            
            try (ResultSet resultSet = statement.executeQuery()) {
                
                DefaultTableModel tableModel = new DefaultTableModel();

             
                tableModel.addColumn("GuestID");
                tableModel.addColumn("Name");
                tableModel.addColumn("room_occupied");
                tableModel.addColumn("Check-in Date");
                tableModel.addColumn("Check-out Date");

                
                while (resultSet.next()) {
                    Vector<Object> rowData = new Vector<>();
                    rowData.add(resultSet.getInt("guestid"));
                    rowData.add(resultSet.getString("name"));
                    rowData.add(resultSet.getInt("room_occupied"));
                    rowData.add(resultSet.getString("check_in"));
                    rowData.add(resultSet.getString("check_out"));

                    tableModel.addRow(rowData);
                }

              
                JTable guestTable = new JTable(tableModel);

                
                int option = JOptionPane.showConfirmDialog(this, new JScrollPane(guestTable), "Select a guest to check out", JOptionPane.OK_CANCEL_OPTION);
                
                if (option == JOptionPane.OK_OPTION) {
                
                    int selectedRow = guestTable.getSelectedRow();
                    
                 
                    if (selectedRow != -1) {
                       
                        int selectedGuestID = (int) tableModel.getValueAt(selectedRow, 0);
                        
                        
                        String updateSql = "UPDATE Guest SET check_out = ? WHERE GuestID = ?";
                        
                        try (PreparedStatement updateStatement = connection.prepareStatement(updateSql)) {
                           
                            updateStatement.setDate(1, new java.sql.Date(System.currentTimeMillis()));
                            updateStatement.setInt(2, selectedGuestID);
                            
                        
                            updateStatement.executeUpdate();
                        }
                        
                        
                        JOptionPane.showMessageDialog(this, "Guest checked out successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            }
        }

      
        connection.close();
    } catch (SQLException ex) {
      //  JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
       // ex.printStackTrace();
   }
}
                
       
